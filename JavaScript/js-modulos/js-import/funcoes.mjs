function mostraIdade(nome, idade) {
    return `${nome} tem ${idade} anos.`;
}

function mostraCidade(nome, cidade) {
    return `${nome} mora em ${cidade}.`;
}

function mostraHobby(nome, hobby) {
    return `${nome} tem o seguinte hobby: ${hobby}.`;
}

export {
    mostraIdade,
    mostraCidade,
    mostraHobby
};